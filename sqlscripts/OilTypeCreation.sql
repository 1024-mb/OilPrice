CREATE TABLE OILPRICES(
	RecordID INT PRIMARY KEY AUTO_INCREMENT,
    DatapointID INT,
    OilID INT,
    Price FLOAT,
    FOREIGN KEY (DatapointID) REFERENCES READING(DatapointID),
    FOREIGN KEY (OilID) REFERENCES OILTYPE(OilID)
);
