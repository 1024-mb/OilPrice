CREATE TABLE READING(
	DatapointID INT PRIMARY KEY AUTO_INCREMENT,
    TimeReading TIME,
    MarketDate DATE

);

CREATE TABLE OILTYPE(
	OilID INT PRIMARY KEY AUTO_INCREMENT,
    OilName VARCHAR(10)
);

